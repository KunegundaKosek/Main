package Sda.Zaawansowane.Task01Dziedziczenie.Toy;

public class Toy {

    private int price;
    private int minAge;

    public Toy(int price, int minAge) {
        this.price = price;
        this.minAge = minAge;
    }

    public int getPrice() {
        return price;
    }

    public int getMinAge() {
        return minAge;
    }
}
